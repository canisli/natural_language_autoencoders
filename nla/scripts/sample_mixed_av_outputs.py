"""Sample a mixed blindable list of real and random AV outputs.

Real rows come from an existing decoded trace JSONL with `nla_output`.
Random rows are generated by sampling unit vectors in model space and decoding
them locally with the AV by default. Pass `--backend sglang` only when you
explicitly want to use an already-running SGLang server.
"""

import argparse
import html
import json
import os
from pathlib import Path
from typing import Any

import numpy as np
import torch

_REPO_ROOT = Path(__file__).resolve().parents[2]
_DEFAULT_HF_HOME = _REPO_ROOT / "hf_cache"
os.environ.setdefault("HF_HOME", str(_DEFAULT_HF_HOME))
os.environ.setdefault("HF_HUB_CACHE", str(_DEFAULT_HF_HOME))
os.environ.setdefault("HF_XET_CACHE", str(_DEFAULT_HF_HOME / "xet"))

from nla.scripts.decode_token_activations import (  # noqa: E402
    SGLangDecoder,
    TransformersDecoder,
    _default_device,
    _torch_dtype,
)


_REAL_META_KEYS = (
    "row_index",
    "token_index",
    "token_id",
    "token_text",
    "char_start",
    "char_end",
    "is_special",
    "prefix_text",
    "activation_norm",
)


def _read_jsonl(path: Path) -> list[dict[str, Any]]:
    rows: list[dict[str, Any]] = []
    with path.open() as f:
        for line_no, line in enumerate(f, start=1):
            line = line.strip()
            if not line:
                continue
            rec = json.loads(line)
            if not isinstance(rec, dict):
                raise ValueError(f"{path}:{line_no}: expected a JSON object")
            rows.append(rec)
    return rows


def read_real_trace(path: Path) -> list[dict[str, Any]]:
    rows = []
    for i, rec in enumerate(_read_jsonl(path)):
        output = rec.get("nla_output")
        if not isinstance(output, str) or not output.strip():
            continue
        row = dict(rec)
        row["_trace_row_index"] = i
        rows.append(row)
    if not rows:
        raise ValueError(f"{path} has no rows with non-empty string nla_output")
    return rows


def split_counts(count: int, allow_odd: bool) -> tuple[int, int]:
    if count <= 0:
        raise ValueError("--count must be positive")
    if count % 2 != 0 and not allow_odd:
        raise ValueError("--count must be even unless --allow-odd is passed")
    real_count = count // 2
    random_count = count - real_count
    return real_count, random_count


def sample_unit_vector(d_model: int, seed: int) -> np.ndarray:
    if d_model <= 0:
        raise ValueError(f"d_model must be positive, got {d_model}")
    rng = np.random.default_rng(seed)
    v = rng.standard_normal(d_model).astype(np.float32)
    norm = float(np.linalg.norm(v))
    if norm == 0.0:
        raise AssertionError("standard_normal produced a zero vector")
    return (v / norm).astype(np.float32)


def _record_kind(kind: str, blind: bool, metadata: dict[str, Any] | None = None) -> dict[str, Any]:
    if not blind:
        return {"kind": kind}
    rec: dict[str, Any] = {"answer_kind": kind}
    if metadata:
        rec["answer_metadata"] = metadata
    return rec


def real_record(row: dict[str, Any], blind: bool) -> dict[str, Any]:
    meta = {"trace_row_index": row["_trace_row_index"]}
    for key in _REAL_META_KEYS:
        if key in row:
            meta[key] = row[key]
    rec = _record_kind("real", blind, meta)
    rec["output"] = row["nla_output"]
    if blind:
        return rec
    rec["trace_row_index"] = row["_trace_row_index"]
    for key in _REAL_META_KEYS:
        if key in row:
            rec[key] = row[key]
    return rec


def random_record(random_index: int, random_seed: int, vector: np.ndarray, output: str, blind: bool) -> dict[str, Any]:
    metadata = {
        "random_index": random_index,
        "random_seed": random_seed,
        "vector_norm": float(np.linalg.norm(vector)),
    }
    rec = _record_kind("random", blind, metadata)
    rec["output"] = output
    if not blind:
        rec.update(metadata)
    return rec


def build_mixed_records(
    real_rows: list[dict[str, Any]],
    decoder: Any,
    args: argparse.Namespace,
) -> list[dict[str, Any]]:
    real_count, random_count = split_counts(args.count, args.allow_odd)
    if len(real_rows) < real_count:
        raise ValueError(
            f"need {real_count} real rows for --count {args.count}, "
            f"but only found {len(real_rows)} valid rows in --real-trace"
        )

    d_model = int(decoder.client.cfg.d_model)
    rng = np.random.default_rng(args.seed)
    real_indices = rng.choice(len(real_rows), size=real_count, replace=False)
    random_seeds = rng.integers(0, 2**63 - 1, size=random_count, dtype=np.int64)

    records: list[dict[str, Any]] = []
    for idx in real_indices.tolist():
        records.append(real_record(real_rows[idx], args.blind))

    for random_index, seed_value in enumerate(random_seeds.tolist()):
        vector = sample_unit_vector(d_model, int(seed_value))
        output = decoder.generate(vector, args)
        records.append(random_record(random_index, int(seed_value), vector, output, args.blind))
        print(f"[random {random_index + 1}/{random_count}] seed={int(seed_value)}", flush=True)

    order = rng.permutation(len(records))
    mixed = [records[i] for i in order.tolist()]
    for sample_index, rec in enumerate(mixed):
        rec["sample_index"] = sample_index
    return mixed


def _json_for_script(value: Any) -> str:
    return json.dumps(value, ensure_ascii=False, separators=(",", ":")).replace("</", "<\\/")


def _visible_kind(rec: dict[str, Any]) -> str:
    return str(rec.get("kind", "?"))


