def _write_jsonl(path: Path, records: list[dict[str, Any]]) -> None:
    path.parent.mkdir(parents=True, exist_ok=True)
    with path.open("w") as f:
        for rec in records:
            f.write(json.dumps(rec, ensure_ascii=False) + "\n")


def _load_decoder(args: argparse.Namespace) -> Any:
    if args.backend == "sglang":
        return SGLangDecoder(args.checkpoint, args.sglang_url)
    return TransformersDecoder(
        args.checkpoint,
        args.device,
        args.torch_dtype,
        device_map=args.device_map,
    )


def main() -> None:
    p = argparse.ArgumentParser(description=__doc__, formatter_class=argparse.RawDescriptionHelpFormatter)
    p.add_argument("--real-trace", required=True, help="JSONL trace with real AV outputs in nla_output.")
    p.add_argument("--checkpoint", required=True, help="Local NLA AV checkpoint dir with nla_meta.yaml.")
    p.add_argument("--output", required=True, help="Output mixed JSONL path.")
    p.add_argument("--html-output", help="Optional HTML viewer output path.")
    p.add_argument("--count", type=int, default=100)
    p.add_argument("--allow-odd", action="store_true", help="Allow odd --count; the extra row is random.")
    p.add_argument("--seed", type=int, default=0)
    p.add_argument("--blind", action="store_true", help="Hide kind in visible fields; preserve answer_kind.")
    p.add_argument("--backend", choices=("sglang", "transformers"), default="transformers")
    p.add_argument("--sglang-url", default="http://localhost:30000")
    p.add_argument("--device", default=_default_device(), help="For --backend transformers: cuda, mps, or cpu.")
    p.add_argument(
        "--device-map",
        default="none",
        help='For --backend transformers: Accelerate device map, e.g. "auto"; "none" moves the whole model to --device.',
    )
    p.add_argument("--torch-dtype", type=_torch_dtype, default=torch.bfloat16)
    p.add_argument("--temperature", type=float, default=0.7)
    p.add_argument("--max-new-tokens", type=int, default=200)
    p.add_argument("--raw", action="store_true", help="Keep raw AV output instead of extracting explanation tags.")
    args = p.parse_args()

    real_rows = read_real_trace(Path(args.real_trace))
    decoder = _load_decoder(args)
    records = build_mixed_records(real_rows, decoder, args)

    out = Path(args.output)
    _write_jsonl(out, records)
    print(f"wrote {len(records)} mixed outputs to {out}", flush=True)

    if args.html_output:
        html_out = Path(args.html_output)
        html_out.parent.mkdir(parents=True, exist_ok=True)
        html_out.write_text(render_html(records, title="Mixed Real/Random AV Outputs", blind=args.blind))
        print(f"wrote HTML viewer to {html_out}", flush=True)


if __name__ == "__main__":
    main()
